package LeetCode;

public class Day3 {

        public int removeDuplicates(int[] nums) {
            for (int i = 0 ; i < nums.length-1;i++)
            {
                if(nums[i] != nums[i+1])
                    continue;

            }
            return 0;
        }

    public static void main(String[] args) {
        System.out.println(1^1^3);
    }
}
